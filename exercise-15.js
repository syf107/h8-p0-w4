function highestScore (students) {

    // membuat variabel result untuk menampung hasil
    var result = {};
    

    // membuat looping untuk menjadikan class sebagai key
    for(var m = 0; m < students.length; m++){ 
        var namaScore = {}
        var nama = '';
        var skor = 0;
        result[students[m].class] = namaScore;

            if(students[m].class && students[m].score > skor ){
                nama = students[m].name;
                skor = students[m].score;   
            }
        namaScore['name'] = nama;
        namaScore['score'] = skor;

    }
    return result;
}
  
