function countProfit(shoppers) {

    let listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                       ['Baju Zoro', 500000, 2],
                       ['Sweater Uniklooh', 175000, 1]
                     ];  

    // pertama conditional if, jika tidak ada isi value
    if(shoppers.length === 0){
        return "//[]"
    }
    
    // pembuatan array yang berisikan penjualan.
    var hasil = [];

    // membuat loop untuk pembuatan object dan aray detil untuk mengisi hasil. 
    for(var m = 0; m < listBarang.length; m++){
        var belanja = {};
        var produk = listBarang[m][0];
        var pembeli = [];
        var sisa = listBarang[m][2];
        var profit = 0;
        for(var n = 0; n < shoppers.length; n++){
            if(listBarang[m][0] === shoppers[n].product){
                pembeli.push(shoppers[n].name);
                if (sisa - shoppers[n].amount >= 0){
                    sisa = sisa - shoppers[n].amount;
                    profit = profit + (listBarang[m][1] * shoppers[n].amount);
                } 
            }    
            belanja['product'] = produk;
            belanja['shoppers'] = pembeli;
            belanja['leftOver'] = sisa;
            belanja['totalProfit'] = profit;            
        }
        hasil.push(belanja);
    
    }
   
    return hasil;
}
  
